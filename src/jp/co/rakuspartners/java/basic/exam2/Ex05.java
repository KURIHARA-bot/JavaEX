package jp.co.rakuspartners.java.basic.exam2;

public class Ex05 {
    public static void main(String[] args) {
        int num1;
        int num2;
        num1 = 30;
        num2 = 5;
        
        int t = (num1 + num2);
        int h = (num1 - num2);
        int k = (num1 * num2);
        int w = (num1 / num2);

        System.out.println(num1 + "+" + num2 + "=" + t);
        System.out.println(num1 + "-" + num2 + "=" + h);
        System.out.println(num1 + "*" + num2 + "=" + k);
        System.out.println(num1 + "/" + num2 + "=" + w);

    }

}
